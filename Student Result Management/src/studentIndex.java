    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ROLLNO = jTextField1.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "mysql");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from student inner join Results where student.ROLLNO='" + ROLLNO + "' and Results.ROLLNO='" + ROLLNO + "'");
            if (rs.next()) {
                setVisible(false);
                new studentHome(ROLLNO).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "ROLL NUMBER NOT FOUND");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "CONNECTION ERROR");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

