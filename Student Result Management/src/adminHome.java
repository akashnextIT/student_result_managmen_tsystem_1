    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
         String COURSE=(String)jComboBox3.getSelectedItem();
        String BRANCH_NAME=(String)jComboBox4.getSelectedItem();
        String ROLLNO=jTextField3.getText();
        String NAME=jTextField1.getText();
        String GENDER=(String)jComboBox6.getSelectedItem();
        String FATHER_NAME=jTextField2.getText();
        
        if(jTextField3.getText().trim().isEmpty() && jTextField1.getText().trim().isEmpty() && jTextField2.getText().trim().isEmpty()) 
        {
            lbl1.setText("PLEASE INSERT ROLL NO.");
            lbl2.setText("NAME IS MANDATORY");
            lbl.setText("Required");
        }
        else if(jTextField3.getText().trim().isEmpty())
        {
            lbl1.setText("PLEASE INSERT ROLL NO.");
        }
        else if(jTextField1.getText().trim().isEmpty())
        {
            lbl2.setText("NAME IS MANDATORY");
        }
        else if(jTextField2.getText().trim().isEmpty())
        {
            lbl.setText("Required");
        }
        else
            try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","mysql");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where ROLLNO='"+ROLLNO+"'");
            if(rs.next())
            {
            st.executeUpdate("update student set COURSE='"+COURSE+"',BRANCH_NAME='"+BRANCH_NAME+"',NAME='"+NAME+"',GENDER='"+GENDER+"',FATHER_NAME='"+FATHER_NAME+"' where ROLLNO='"+ROLLNO+"'");
            JOptionPane.showMessageDialog(null,"SUCCESSFULLY UPDATED");
            setVisible(false);
            new adminHome().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Roll Number does not exist in our database");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Connection error");
            setVisible(false);
            new adminHome().setVisible(true);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

